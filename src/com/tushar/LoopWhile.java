package com.tushar;

// while loop Syntax
//while (condition) {
//    // body
//     //   iteration
//        }



public interface LoopWhile {
    public static void main(String[] args) {
        int count = 1;
        while(count != 20){ // condition ( boolean ) // jab tak (cond) tab tak
            System.out.println(count); // (body/task)  ye karo
            count++; // (iteration count ) // ++ ya --
        }
    }
}
